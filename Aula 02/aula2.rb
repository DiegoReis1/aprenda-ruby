# Operações aritimeticas

variavel = 2 - 2 #Multiplicação
segunda_variavel = 2 + 1 # Adição
terceira_variavel = variavel + segunda_variavel # Operações com variaveis
puts terceira_variavel # 3

quarta_variavel = 6 / 2 # divisão
quinta_variavel = quarta_variavel * 5 #Multiplicação
puts quinta_variavel

sexta_variavel = 2**2 # Exponênciação
puts sexta_variavel # 4